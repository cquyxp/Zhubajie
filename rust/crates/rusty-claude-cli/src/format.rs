#[allow(clippy::struct_field_names)]
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]
pub(crate) struct GitWorkspaceSummary {
    pub(crate) changed_files: usize,
    pub(crate) staged_files: usize,
    pub(crate) unstaged_files: usize,
    pub(crate) untracked_files: usize,
    pub(crate) conflicted_files: usize,
}

impl GitWorkspaceSummary {
    pub(crate) fn is_clean(self) -> bool {
        self.changed_files == 0
    }

    pub(crate) fn headline(self) -> String {
        if self.is_clean() {
            "clean".to_string()
        } else {
            let mut details = Vec::new();
            if self.staged_files > 0 {
                details.push(format!("{} staged", self.staged_files));
            }
            if self.unstaged_files > 0 {
                details.push(format!("{} unstaged", self.unstaged_files));
            }
            if self.untracked_files > 0 {
                details.push(format!("{} untracked", self.untracked_files));
            }
            if self.conflicted_files > 0 {
                details.push(format!("{} conflicted", self.conflicted_files));
            }
            format!(
                "dirty · {} files · {}",
                self.changed_files,
                details.join(", ")
            )
        }
    }
}

pub(crate) fn format_model_report(
    model: &str,
    provider: Option<&str>,
    alias: Option<&str>,
    routing: Option<&str>,
    config_summary: Option<&str>,
    message_count: usize,
    turns: u32,
) -> String {
    let mut lines = Vec::with_capacity(8);
    lines.push(format!("Model\n  Current          {model}"));
    lines.push(format!(
        "  Provider         {}",
        provider.unwrap_or("<unknown>")
    ));
    if let Some(alias) = alias {
        lines.push(format!("  Alias            {alias}"));
    }
    if let Some(routing) = routing {
        lines.push(format!("  Routing          {routing}"));
    }
    lines.push(format!(
        "  Config           {}",
        config_summary.unwrap_or("default model configuration")
    ));
    lines.push(format!(
        "  Session          {message_count} messages · {turns} turns"
    ));
    lines.push(String::new());
    lines.push("Usage".to_string());
    lines.push("  Open the model picker with /model".to_string());
    lines.push("  Switch models with /model <name>".to_string());
    lines.join("\n")
}

